# Mengakses Anggota Tuple

my_tuple = ('p', 'r', 'o', 'g', 'r', 'a', 'm', 'm', 'i', 'n', 'g')
# akses dari indeks 0 s/d 2

# Output: ('p', 'r', 'o')
print(my_tuple[:3])

# Akses dari indeks 2 s/d 5
# output: ('o', 'g', 'r', 'a')
print(my_tuple[2:6])

# Akses dari indeks 3 sampai akhir
# output: ('g', 'r', 'a', 'm', 'm', 'i', 'n', 'g')
print(my_tuple[3:])